                </div> 
            </li>
            <li> 
                <a href="#suscriptor" class="inf">Datos del suscriptor</a>
                <div id="suscriptor" class="oculto">
                    <p>En esta área se diligencian los datos de la persona que a tomar el servicio</p>
                    <img src="../files/ayuda/contrato/partes_datos_suscriptor.PNG" alt="Datos del suscriptor" style="width: 100%; height: auto;">
                    <p>La mayoría de los datos son básicos, pero también encontrara el campo de “Tipo de cliente” el cual determina si el cliente que registrara en nuevo contrato pertenece a algún aliado o es directo de la compañía.</p>
                    <img src="../files/ayuda/contrato/partes_datos_suscriptor_1.PNG" alt="">
                    <p>Algunos de los datos que se encuentran en esta área son muy similares, esto se debe a que los datos correspondientes a la persona que realiza el contrato podrían ser diferentes a los datos del sitio de instalación del servicio</p>
                    <img src="../files/ayuda/contrato/partes_datos_suscriptor_2.PNG" alt="" style="width: 100%; height: auto;">
