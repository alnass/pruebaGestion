                    <img src="../files/ayuda/contrato/partes_productos_1.PNG" alt="" style="width: 100%; height: auto;">
                    <p>Para agregar un producto se selecciona de los productos de una ventana emergente que cuenta con el listado de productos disponibles para esa sede</p>
                    <img src="../files/ayuda/contrato/partes_productos_2.PNG" alt="" style="width: 100%; height: auto;">
                    <p>Al seleccionar el producto este pasara al listado que está ubicado en la parte inferior de la venta</p>
                    <img src="../files/ayuda/contrato/partes_productos_3.PNG" alt="" style="width: 100%; height: auto;">
                    <p>Desde esta tabla podrá eliminar el ítem seleccionado en caso de ser erróneo y podrá repetir la operación desde el botón de “Agregar producto”</p>
                    <img src="../files/ayuda/contrato/partes_productos_4.PNG" alt="" style="width: 100%; height: auto;">
                    <p>Podrá modificar la cantidad de los servicios seleccionados. En caso de modificar la cantidad deberá actualizar para obtener el valor real de los servicios</p>
