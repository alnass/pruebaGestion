                    <p>En el menú lateral encontrará el ítem “Contratos”.</p>
                    <img src="../files/ayuda/contrato/ingreso1.PNG" alt="">
                    <p>Al dar clic sobre este ítem de desplegar un submenú en que deberá seleccionar “Gestión de contrato”</p>
                    <img src="../files/ayuda/contrato/ingreso2.PNG" alt="">
                    <p>Posterior a esto de desplegar una ventana en la que encontrara un listado de los contratos ya creados y la opción de crear uno nuevo</p>
                    <img src="../files/ayuda/contrato/ingreso3.PNG" alt="" style="width: 100%; height: auto;">
                    <p>Finalmente, diligencie el formulario que aparece a continuación con los datos del suscriptor y los datos correspondientes al contrato</p>
