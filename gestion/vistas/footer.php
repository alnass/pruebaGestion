                    <img src="../files/ayuda/contrato/partes_datos_contrato.PNG" alt="" style="width: 100%; height: auto;">
                    <p>Algunos de los campos se encuentran pre llenos con unos valores predeterminados, los cuales solo se cambiarán en caso de ser necesario</p>
                    <img src="../files/ayuda/contrato/partes_datos_contrato_1.PNG" alt="" style="width: 100%; height: auto;">
                </div>
            </li>
