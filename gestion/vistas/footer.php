<div class="modal fade " id="ModalRecaudo" tabindex="-1" role="dialog" aria-labelledby="myLargeModalLabel" aria-hidden="true">
  <div class="modal-dialog modal-lg">
    <div class="modal-content">
        <div class="modal-header">
            <h2>Ayuda para realizar recaudos</h2>
        </div>
        <div class="modal-body">
        
        <ul>
            <h3>Pagos</h3>
            <li>
                <a href="#info_1" class="inf">Nuevo recaudo</a>
                <div id="info_1" class="oculto">
                    <img src="../files/ayuda/recaudo/recaudo_1.PNG" alt="">
                    <img src="../files/ayuda/recaudo/recaudo_2.PNG" alt="">
                    <img src="../files/ayuda/recaudo/recaudo_3.PNG" alt="" style="width: 100%; height: auto;">
                    
                </div> 
            </li>
            <li> 
                <a href="#info_2" class="inf">Partes del formulario de recaudo</a>
                <div id="info_2" class="oculto">
                    <p>Datos del contrato, del suscriptor y los servicios con los que cuenta</p>
                    <img src="../files/ayuda/recaudo/recaudo_4.PNG" alt="">
                    <p>En esta parte del formulario están los costos y valores como: valor de la mensualidad, saldo anterior, saldo actual, aplicación de pronto paga etc.</p>
                    <img src="../files/ayuda/recaudo/recaudo_5.PNG" alt="" style="width: 100%; height: auto;">
                    <p>En la parte inferior encontrara el estado de cuenta general del suscriptor</p>
                    <img src="../files/ayuda/recaudo/recaudo_6.PNG" alt="" style="width: 100%; height: auto;">
                </div>
            </li>

            <li>
                <a href="#info_3" class="inf">Aplicación del pronto pago</a>
                <div id="info_3" class="oculto">
                    <p>El pronto pago se aplica de forma automática al pago siempre y cuando cumpla con las condiciones establecidas. <br> Inicialmente el producto debe estar habilitado para el descuento de pronto pago en la sede. <br>El pago debe realizarse entre la fecha establecida para adquirir este beneficio, si se encuentra fuera de fecha el sistema mostrara lo siguiente</p>
                    <img src="../files/ayuda/recaudo/recaudo_7.PNG" alt="">
                    <p>Si la fecha es hábil para realizar el descuento el sistema calculara de forma automática el valor correspondiente al producto mostrándolo de esta forma</p>
                    <img src="../files/ayuda/recaudo/recaudo_8.PNG" alt="">
                    <p>Si el suscriptor tiene un saldo anterior el sistema calcula el valor de pronto pago solo sobre el valor correspondiente al mes en curso y muestra el valor total y el valor a cancelar para poder aplicar el descuento</p>
                    <img src="../files/ayuda/recaudo/recaudo_9.PNG" alt="">
                    <p>En caso que el suscriptor no tenga saldo a pagar o el saldo no supere el valor del pronto pago se presentara de la siguiente forma</p>
                    <img src="../files/ayuda/recaudo/recaudo_10.PNG" alt="">
                    <p>Después de haber realizado el pago, la transacción se verá reflejada en el estado de cuenta del suscriptor de la siguiente forma</p>
                    <img src="../files/ayuda/recaudo/recaudo_11.PNG" alt="" style="width: 100%; height: auto;">
                </div>
            </li>
            <li>
                <a href="#info_4" class="inf">Generar el pago</a>
                <div id="info_4" class="oculto">
                    <p>Inicialmente se debe seleccionar el “CONCEPTO DE LA TRANSACCIÓN” desde el campo e lista</p>
                    <img src="../files/ayuda/recaudo/recaudo_12.PNG" alt="">
                    <p>Si el concepto es “EFECTIVO” solo deberá diligenciar el campo de “RECAUDO” con el valor que el suscriptor va a cancelar</p>
                    <img src="../files/ayuda/recaudo/recaudo_13.PNG" alt="">
                    <p>En caso de seleccionar cualquier otro medio de pago el sistema habilitara los campos de “NO DE COMPROBANTE” y “FECHA DE TRANSACCIÓN”, los cuales deben ser diligenciados de forma obligatoria junto con el valor a cancelar en el campo “RECAUDO” </p>
                    <img src="../files/ayuda/recaudo/recaudo_14.PNG" alt="">
                    <p>El campo de “OBSERVACIÓN” puede ser diligenciado de forma opcional, si el usuario del sistema considera pertinente</p>
                    <img src="../files/ayuda/recaudo/recaudo_15.PNG" alt="">
                    <p>Al finalizar el diligenciamiento de los campos puede guardar la transacción con la tecla “ENTER” o directamente desde el botón de “GUARDAR” en la parte inferior izquierda</p>
                    <img src="../files/ayuda/recaudo/recaudo_16.PNG" alt="" style="width: 100%; height: auto;">
                    <p>Al guardar la transacción automáticamente se generará el tiquete de pago que deberá imprimir</p>
                    <img src="../files/ayuda/recaudo/recaudo_17.PNG" alt="">
                    <p>Para imprimir el tiquete puede buscar el botón de “IMPRIMIR” en la esquina superior derecha de la ventana o con la combinación de teclas “CTRL + P”</p>
                    <img src="../files/ayuda/recaudo/recaudo_18.PNG" alt="">
                    <p>Posterior a esto aparecerá una ventana en la que deberá cerciorarse que la impresora corresponda a la instalada para impresión de los tiquetes y generar la impresión del mismo </p>
                    <img src="../files/ayuda/recaudo/recaudo_19.PNG" alt="">
                    <p>Finalmente, para ir de nuevo al módulo de recaudo deberá hacerlo mediante el botón de “RETROCEDER” de su navegador, ubicado a la derecha de la barra de dirección en la parte superior derecha</p>
                    <img src="../files/ayuda/recaudo/recaudo_20.PNG" alt="">
                </div>
            </li>
            <h3>Cierres</h3>
            <li>
                <a href="#info_5" class="inf">Cierre parcial</a>
                <div id="info_5" class="oculto">
                    <p>El cierre parcial muestra los movimientos del día y mantiene la caja abierta para continuar con los registros </p>
                    <img src="../files/ayuda/recaudo/recaudo_1.PNG" alt="">
                    <img src="../files/ayuda/recaudo/recaudo_21.PNG" alt="">                    
                    <p>Al dar clic en “CIERRE PARCIAL” se abrirá una ventana de impresión en una nueva pestaña con la información de los registros del día</p>
                    <img src="../files/ayuda/recaudo/recaudo_22.PNG" alt="" style="width: 100%; height: auto;">
                    <p>En caso que desee imprimir y la pagina no se encuentre configurada deberá seleccionar la impresora correspondiente al tamaño de hoja que desea imprimir</p>
                    <img src="../files/ayuda/recaudo/recaudo_23.PNG" alt="">
                    <p>Seleccione la impresora desde el listado</p>
                    <img src="../files/ayuda/recaudo/recaudo_24.PNG" alt="" style="width: 100%; height: auto;">
                    <p>En caso de no querer imprimir el cierre parcial solo debe oprimir la tecla “ESC” de la esquina superior derecha del teclado, y se presentara solamente la información en la ventana</p>
                    <img src="../files/ayuda/recaudo/recaudo_25.PNG" alt="" style="width: 100%; height: auto;">
                    <p>Para retornar a la pantalla de gestión oprima el botón de retroceder de su navegador </p>
                    <img src="../files/ayuda/recaudo/recaudo_30.PNG" alt="">
                </div>
            </li>
            <li>
                <a href="#info_6" class="inf">Cierre final</a>
                <div id="info_6" class="oculto">
                    <p>El “CIERRE FINAL” es el cierre que finaliza la operación del día, al realizar este cierre se bloquea el acceso al ítem de “RECAUDO”, por esta razón solo debe realizarse al final todos los registros del día</p>
                    <img src="../files/ayuda/recaudo/recaudo_1.PNG" alt="">
                    <img src="../files/ayuda/recaudo/recaudo_26.PNG" alt="">
                    <p>Al dar clic en “CIERRE FINAL” se despliega una ventana emergente en la que deberá confirmar el cierre de operaciones, si cancela la ventana “NO” se realizará el cierre final</p>
                    <img src="../files/ayuda/recaudo/recaudo_27.PNG" alt="">
                    <p>Se abrirá una ventana de impresión en una nueva pestaña con la información de los registros finales del día</p>
                    <img src="../files/ayuda/recaudo/recaudo_28.PNG" alt="" style="width: 100%; height: auto;">
                    <p>En caso que desee imprimir y la pagina no se encuentre configurada deberá seleccionar la impresora correspondiente al tamaño de hoja que desea imprimir</p>
                    <img src="../files/ayuda/recaudo/recaudo_23.PNG" alt="">
                    <p>Seleccione la impresora desde el listado</p>
                    <img src="../files/ayuda/recaudo/recaudo_24.PNG" alt="" style="width: 100%; height: auto;">
                    <p>En caso de no querer imprimir el cierre parcial solo debe oprimir la tecla “ESC” de la esquina superior derecha del teclado, y se presentara solamente la información en la ventana</p>
                    <img src="../files/ayuda/recaudo/recaudo_29.PNG" alt="" style="width: 100%; height: auto;">
                    <p>Para retornar a la pantalla de gestión oprima el botón de retroceder de su navegador </p>
                    <img src="../files/ayuda/recaudo/recaudo_30.PNG" alt="">
                </div>
            </li>
        </ul>
